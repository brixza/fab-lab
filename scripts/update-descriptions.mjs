import { createReadStream, writeFileSync } from 'fs'
import { parse } from 'csv-parse'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const CSV_PATH = join(__dirname, '..', 'products_export_1.csv')
const OUT_PATH = join(__dirname, '..', 'update_descriptions.sql')

function escape(str) {
  if (!str) return ''
  return str.replace(/'/g, "''")
}

function stripHtml(html) {
  return html.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim()
}

function parseBodyHtml(html) {
  if (!html) return { description: null, scent_family: null }

  const familyMatch = html.match(/Doftfamilj[:\s]+([^<]+)/i)
  const scent_family = familyMatch ? familyMatch[1].trim() : null

  const paragraphs = [...html.matchAll(/<p[^>]*>([\s\S]*?)<\/p>/gi)]
    .map(m => stripHtml(m[1]))
    .filter(t => t.length > 80 && !/doftfamilj/i.test(t) && !/toppnot|hjärtnot|basnot/i.test(t))
  const description = paragraphs[0] ?? null

  return { description, scent_family }
}

const seen = new Map() // handle → { description, scent_family }

const parser = createReadStream(CSV_PATH).pipe(
  parse({ columns: true, skip_empty_lines: true, relax_quotes: true, relax_column_count: true })
)

parser.on('data', (row) => {
  const handle = row['Handle']
  if (!handle) return
  if (seen.has(handle)) return // only need first row per product (has the body html)

  const status = (row['Status'] || '').toLowerCase()
  if (status && status !== 'active') return

  const bodyHtml = row['Body (HTML)'] || ''
  const { description, scent_family } = parseBodyHtml(bodyHtml)

  if (description || scent_family) {
    seen.set(handle, { description, scent_family })
  }
})

parser.on('end', () => {
  const entries = [...seen.entries()]
  console.log(`Found descriptions for ${entries.length} products`)

  const lines = [
    '-- Update product descriptions and scent families from Shopify CSV',
    '-- Generated by scripts/update-descriptions.mjs',
    '',
  ]

  for (const [sku, { description, scent_family }] of entries) {
    const desc = description ? `'${escape(description)}'` : 'NULL'
    const family = scent_family ? `'${escape(scent_family)}'` : 'NULL'
    lines.push(`UPDATE products SET description = ${desc}, scent_family = ${family} WHERE sku = '${escape(sku)}';`)
  }

  writeFileSync(OUT_PATH, lines.join('\n') + '\n', 'utf8')
  console.log(`✓ Written to update_descriptions.sql (${entries.length} updates)`)
  console.log('Paste the contents into the Supabase SQL Editor to apply.')
})

parser.on('error', (err) => {
  console.error('CSV parse error:', err.message)
})
